package vdee.evalverde.vdee.data.models;

public class Verse {

    private String verse;
    private float verseNumber;

    public String getVerse() {
        return verse;
    }

    public float getVerseNumber() {
        return verseNumber;
    }


    public void setVerse(String verse) {
        this.verse = verse;
    }

    public void setVerse_nr(float verseNumber) {
        this.verseNumber = verseNumber;
    }
}
